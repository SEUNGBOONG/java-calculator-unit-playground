package Calculate;

public class Calculation {
	int first_num;
	int second_num;

	public int plusCalculate() {
		return first_num + second_num;
	}

	public int minusCalculate() {
		return first_num - second_num;
	}

	public int divCalculate() {
		return first_num / second_num;
	}

	public int multiCalculate() {
		return first_num * second_num;
	}
}
